/**
 * 全キャラの客観記憶から、次の生成に関連する記憶タグを選別
 * @param {string} recentText - 直近のキャラ本文合成テキスト
 * @returns {Promise<string[]>} 選別されたタグの配列
 */
async function selectRelevantMemories(recentText) {
  // 全キャラの客観記憶要約リストを構築
  let memoryList = '';
  state.situation.who.forEach(char => {
    if (!char.active || !char.memories || char.memories.length === 0) return;
    memoryList += '\n【' + (char.base.name || '?') + 'の記憶】\n';
    char.memories.forEach(mem => {
      const tags = (mem.source || []).join(', ');
      memoryList += '- [' + tags + '] ' + mem.summary + '\n';
    });
  });

  if (!memoryList.trim()) return [];

  const body = {
    system_instruction: { parts: [{ text:
      '以下はキャラクターの記憶要約リストです。各記憶には出典タグが付いています。\n\n' +
      memoryList +
      '\n\n【直近の物語テキスト】\n' + (recentText || '（なし — 初回生成）') +
      '\n\n【タスク】\n直近の展開を読み、次の物語生成に参照すべき過去の記憶を選んでください。\n\n' +
      '【選別基準】\n1. 人物の再会・対面（Who一致）\n2. 場所の再訪（Where一致）\n3. 因果関係（動機・原因）\n4. 状況の類似（戦闘・交渉等）\n5. 未解決の伏線\n\n' +
      '【出力形式】JSONのみ。\n{"tags": ["A-003-ルミア", "B-012-エルザ", ...]}'
    }] },
    contents: [{ role: 'user', parts: [{ text: '上記の記憶リストから関連する記憶タグを選別してください。' }] }],
    generationConfig: { temperature: 0.2, maxOutputTokens: 1024 },
    safetySettings: SAFETY_OFF
  };

  try {
    const data = await textApiFetch(body, '記憶選別');
    const text = data.candidates?.[0]?.content?.parts?.[0]?.text || '{}';
    const cleaned = text.replace(/```json\n?/g, '').replace(/```\n?/g, '').trim();
    const result = safeParseJSON(cleaned, '記憶選別');
    return result.tags || [];
  } catch (e) {
    addLog('⚠️ 記憶選別失敗（全記憶を使用）: ' + e.message, 'warning');
    // フォールバック: 全タグを返す
    const allTags = [];
    state.situation.who.forEach(char => {
      if (!char.active || !char.memories) return;
      char.memories.forEach(mem => {
        if (mem.source) allTags.push(...mem.source);
      });
    });
    return allTags;
  }
}

