/**
 * 現在のシーンの全キャラ本文を合成して直近テキストを生成
 * @param {number} maxChars - 最大文字数
 * @returns {Promise<string>}
 */
async function getRecentTextFromDB(maxChars) {
  maxChars = maxChars || 3000;
  try {
    const texts = await loadCharacterTextsByScene(state.currentScene.key);
    if (!texts || texts.length === 0) return '';

    // 全段落をイベント番号順に統合
    const allParas = [];
    texts.forEach(ct => {
      if (!ct.paragraphs) return;
      Object.entries(ct.paragraphs).forEach(([tag, para]) => {
        const r = resolveTag(tag);
        if (r) {
          allParas.push({ tag, num: parseInt(r.num, 10), char: r.entity, text: para.text || '' });
        }
      });
    });
    allParas.sort((a, b) => a.num - b.num || a.char.localeCompare(b.char));

    let result = '';
    for (const p of allParas) {
      const line = '[' + p.tag + '] ' + p.text + '\n\n';
      if (result.length + line.length > maxChars) break;
      result += line;
    }
    return result;
  } catch (e) {
    console.error('getRecentTextFromDB error:', e);
    return '';
  }
}
