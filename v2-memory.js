/**
 * 客観記憶を登録（state + IndexedDB両方に保存）
 */
async function registerObjectiveMemory(charName, entry) {
  // state側に追加
  const charIdx = state.situation.who.findIndex(c => c.base.name === charName);
  if (charIdx >= 0) {
    if (!state.situation.who[charIdx].memories) state.situation.who[charIdx].memories = [];
    state.situation.who[charIdx].memories.push({
      id: entry.id || ('mem_' + Date.now() + '_' + Math.random().toString(36).slice(2, 6)),
      summary: entry.summary,
      source: entry.source || [],
      scene: entry.scene || '',
      autoGenerated: true,
      timestamp: entry.timestamp || Date.now()
    });
  }
  // IndexedDB側に保存
  await saveObjectiveMemory(charName, entry);
}

/**
 * state.situation.who[i].memoriesからUI操作で記憶を追加
 */
function addMemoryEntry(charIdx, summary, autoGenerated, silent, source, scene) {
  autoGenerated = autoGenerated || false;
  silent = silent || false;
  const char = state.situation.who[charIdx];
  if (!char) return;
  if (!char.memories) char.memories = [];
  const mem = {
    id: 'mem_' + Date.now() + '_' + Math.random().toString(36).slice(2, 6),
    summary: summary,
    source: source || [],
    scene: scene || '',
    autoGenerated: autoGenerated,
    timestamp: Date.now()
  };
  char.memories.push(mem);
  if (!silent) addLog('💭 記憶追加: ' + (char.base.name || 'Char' + charIdx) + ' — ' + summary.substring(0, 40), 'info');
  saveToStorage();
  // IndexedDBにも保存
  if (char.base.name) {
    saveObjectiveMemory(char.base.name, mem).catch(e => console.error('Memory save error:', e));
  }
}

function deleteMemoryEntry(charIdx, memId) {
  const char = state.situation.who[charIdx];
  if (!char || !char.memories) return;
  const before = char.memories.length;
  char.memories = char.memories.filter(m => m.id !== memId);
  if (char.memories.length < before) {
    addLog('🗑️ 記憶削除: ' + (char.base.name || 'Char' + charIdx), 'info');
    saveToStorage();
    renderCharacters();
    if (char.base.name) {
      deleteObjectiveMemory(char.base.name, memId).catch(e => console.error('Memory delete error:', e));
    }
  }
}

